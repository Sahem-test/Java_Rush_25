package main.java.core.level04.task67;

import java.util.Scanner;

/**
 * Пользователь вводит с клавиатуры целое число n (больше 1). Ваша задача — проверить, является ли это число простым (делится только на 1 и на само себя).
 * Используйте цикл for, чтобы найти первый делитель этого числа (от 2 до n-1). Как только делитель найден, выведите его на экран и завершите цикл. Если делитель не найден, выведите "Число простое".
 * Требования:
 * •	Программа должна считать с клавиатуры целое число n больше 1.
 * •	Для поиска делителя числа n необходимо использовать цикл for, который перебирает значения от 2 до n-1 включительно.
 * •	Если найден делитель числа n (то есть n делится на i без остатка), программа должна вывести это значение i на экран.
 * •	После нахождения первого делителя программа должна немедленно завершить цикл с помощью оператора break.
 * •	Если цикл завершился и не было найдено ни одного делителя, программа должна вывести на экран сообщение "Число простое".
 */
public class Task67 {
    public static void main(String[] args) {
        // Создаем объект Scanner для считывания данных с клавиатуры
        Scanner console = new Scanner(System.in);

        // Просим пользователя ввести целое число больше 1
        System.out.println("Введите целое число больше 1:");
        int n = console.nextInt();

        // Проверяем, является ли число простым
        boolean isPrime = true; // Флаг для проверки простоты числа

        // Цикл для поиска первого делителя числа n
        for (int i = 2; i < n; i++) {
            if (n % i == 0) { // Если n делится на i без остатка
                System.out.println(i); // Выводим первый найденный делитель
                isPrime = false; // Устанавливаем флаг, что число не простое
                break; // Завершаем цикл, так как делитель найден
            }
        }

        // Если делитель не найден, выводим, что число простое
        if (isPrime) {
            System.out.println("Число простое");
        }
    }
}
