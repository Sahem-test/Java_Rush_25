package main.java.core.level05.Task78;

import java.util.Scanner;

/**
 * В старинном замке нашли свиток с загадочным словом, и легенда гласит, что оно обладает особой силой,
 * если читается одинаково как слева направо, так и справа налево.
 * Вы — криптограф, которому поручено проверить, является ли это слово истинным "палиндромом".
 * Пользователь введёт вам эту загадочную строку (например, ancientRiddle).
 * Ваша программа должна тщательно проанализировать её. Если послание читается одинаково в обоих направлениях,
 * выведите "YES", подтверждая его магическую природу.
 * В противном случае, если оно не является палиндромом, выведите "NO".
 * Требования:
 * •	Программа должна принимать строку, введённую пользователем с клавиатуры.
 * •	Программа должна определить, является ли введённая строка палиндромом (читается одинаково слева направо и справа налево).
 * •	Если строка — палиндром, программа должна вывести "YES". Если строка не является палиндромом, программа должна вывести "NO".
 * •	Проверка должна учитывать регистр символов (например, "Anna" и "anna" считаются разными).
 * •	Программа не должна выводить никаких других сообщений, кроме "YES" или "NO"
 */
public class Task78 {
    public static void main(String[] args) {
        Scanner console = new Scanner(System.in);
        System.out.print("Введите слово: ");
        String word = console.nextLine();
        String result = "";
        String result2 = "";
        for (int i = 0; i < word.length(); i++) {
            result += word.charAt(i);
        }
        for (int j = word.length() - 1; j >= 0; j--) {
            result2 += word.charAt(j);
        }
        if (result.equals(result2)) {
            System.out.println("YES");
        } else {
            System.out.println("NO");
        }

    }
}

