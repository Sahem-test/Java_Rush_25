package main.java.core.level05.task77;

import java.util.Scanner;

/**
 * Вы — архитектор света, которому поручено создать сверкающий, идеально симметричный обелиск из звёзд, который будет сиять в ночном небе.
 * Его высота определяется магическим числом, которое задаст пользователь. Каждая строка обелиска должна быть шире предыдущей, создавая эффект возрастающего сияния.
 * Напишите программу, которая по заданному числу N (назовите его pyramidHeight) изобразит его из звёздочек *.
 * Обелиск должен быть равнобедренным, а символы * равномерно распределены, чтобы создать правильную форму треугольника.
 * Например, если pyramidHeight равно 3, ваш звёздный обелиск будет выглядеть так:
 * *
 * ***
 * *****
 * Требования:
 * •	Программа должна запросить у пользователя ввод целого числа, обозначающего высоту обелиска (`pyramidHeight`).
 * •	Программа должна использовать цикл для построения каждой строки обелиска от первой до `pyramidHeight` включительно.
 * •	В каждой строке должно быть вычислено и выведено нужное количество пробелов (для выравнивания) и звездочек `*`, чтобы обелиск был равнобедренным.
 * •	Программа должна вывести обелиск на экран, чтобы его форма соответствовала примеру из условия задачи.
 */
public class Task77 {
    public static void main(String[] args) {
        Scanner console = new Scanner(System.in);
        System.out.print("Задайте высоту пирамиды: ");
        int pyramidHeight = console.nextInt();
        for (int i = 1; i <= pyramidHeight; i++) {
            for (int j = i; j < pyramidHeight; j++) {
                System.out.print(".");
            }
            for (int k = 1; k <= (2 * i - 1); k++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
