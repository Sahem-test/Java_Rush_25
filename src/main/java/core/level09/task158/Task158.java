package main.java.core.level09.task158;

/**
 * Форматирование числа с плавающей точкой
 * Вы работаете над кассовым аппаратом для элитного бутика. Когда покупатель выбирает товар, цена должна отображаться безупречно, с точностью до двух знаков после запятой,
 * как это принято в финансовых операциях. Никаких лишних цифр, только точность и ясность.
 * Ваша цель — объявить переменную типа double для хранения цены, например, double productPrice со значением 123.456,
 * а затем, используя String.format, вывести её на экран в виде "Цена: 123.46 евро.", гарантируя, что после запятой останется ровно две цифры, как это принято в мире финансов.
 * Требования:
 * •	В программе должна быть объявлена переменная типа double для хранения цены товара.
 * •	Переменная productPrice должна быть инициализирована значением 123.456.
 * •	Для форматирования строки с ценой должно использоваться именно String.format.
 * •	Результат форматирования должен содержать ровно две цифры после запятой.
 * •	На экран должно быть выведено сообщение в таком виде: "Цена: 123.46 евро."
 */
public class Task158 {
    public static void main(String[] args) {
        double productPrice = 123.456;
        System.out.println(String.format("Цена: %.2f евро.", productPrice));
    }
}
